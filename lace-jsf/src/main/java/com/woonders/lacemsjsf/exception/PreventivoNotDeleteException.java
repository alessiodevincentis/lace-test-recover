package com.woonders.lacemsjsf.exception;

public class PreventivoNotDeleteException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8584137933759279379L;

	public PreventivoNotDeleteException() {
		super();
	}

	public PreventivoNotDeleteException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PreventivoNotDeleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public PreventivoNotDeleteException(String message) {
		super(message);
	}

	public PreventivoNotDeleteException(Throwable cause) {
		super(cause);
	}

}
