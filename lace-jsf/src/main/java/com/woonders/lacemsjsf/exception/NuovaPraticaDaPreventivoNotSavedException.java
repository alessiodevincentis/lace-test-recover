package com.woonders.lacemsjsf.exception;

public class NuovaPraticaDaPreventivoNotSavedException extends Exception {

	private static final long serialVersionUID = -155353728879190665L;

	public NuovaPraticaDaPreventivoNotSavedException() {
		super();
	}

	public NuovaPraticaDaPreventivoNotSavedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NuovaPraticaDaPreventivoNotSavedException(String message, Throwable cause) {
		super(message, cause);
	}

	public NuovaPraticaDaPreventivoNotSavedException(String message) {
		super(message);
	}

	public NuovaPraticaDaPreventivoNotSavedException(Throwable cause) {
		super(cause);
	}

}
