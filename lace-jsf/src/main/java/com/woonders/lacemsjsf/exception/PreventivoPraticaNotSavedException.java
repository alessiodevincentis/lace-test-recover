package com.woonders.lacemsjsf.exception;

public class PreventivoPraticaNotSavedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9183032481422887568L;

	public PreventivoPraticaNotSavedException() {
		super();
	}

	public PreventivoPraticaNotSavedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PreventivoPraticaNotSavedException(String message, Throwable cause) {
		super(message, cause);
	}

	public PreventivoPraticaNotSavedException(String message) {
		super(message);
	}

	public PreventivoPraticaNotSavedException(Throwable cause) {
		super(cause);
	}

}
