package com.woonders.lacemsjsf.exception;

public class ValutazioneNotDeleteException extends Exception {

	private static final long serialVersionUID = 390618096457258019L;

	public ValutazioneNotDeleteException() {
		super();
	}

	public ValutazioneNotDeleteException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ValutazioneNotDeleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public ValutazioneNotDeleteException(String message) {
		super(message);
	}

	public ValutazioneNotDeleteException(Throwable cause) {
		super(cause);
	}

}
