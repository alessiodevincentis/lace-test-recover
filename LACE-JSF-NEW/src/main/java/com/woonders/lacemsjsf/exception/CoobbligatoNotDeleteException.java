package com.woonders.lacemsjsf.exception;

public class CoobbligatoNotDeleteException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6010014883508564846L;

	public CoobbligatoNotDeleteException() {
		super();
	}

	public CoobbligatoNotDeleteException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CoobbligatoNotDeleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public CoobbligatoNotDeleteException(String message) {
		super(message);
	}

	public CoobbligatoNotDeleteException(Throwable cause) {
		super(cause);
	}

}
