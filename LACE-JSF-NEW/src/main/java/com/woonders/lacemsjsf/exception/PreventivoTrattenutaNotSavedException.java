package com.woonders.lacemsjsf.exception;

public class PreventivoTrattenutaNotSavedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8737245583090844047L;

	public PreventivoTrattenutaNotSavedException() {
		super();
	}

	public PreventivoTrattenutaNotSavedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PreventivoTrattenutaNotSavedException(String message, Throwable cause) {
		super(message, cause);
	}

	public PreventivoTrattenutaNotSavedException(String message) {
		super(message);
	}

	public PreventivoTrattenutaNotSavedException(Throwable cause) {
		super(cause);
	}

}
