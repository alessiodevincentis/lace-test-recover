package com.woonders.lacemsjsf.exception;

public class FieldPdfNotFoundException extends Exception {

	private static final long serialVersionUID = 4829199531225875287L;

	public FieldPdfNotFoundException() {
		super();
	}

	public FieldPdfNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FieldPdfNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public FieldPdfNotFoundException(String message) {
		super(message);
	}

	public FieldPdfNotFoundException(Throwable cause) {
		super(cause);
	}

}
