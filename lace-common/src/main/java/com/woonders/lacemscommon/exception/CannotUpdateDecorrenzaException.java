package com.woonders.lacemscommon.exception;

public class CannotUpdateDecorrenzaException extends Exception {

	public CannotUpdateDecorrenzaException() {
		super();
	}

	public CannotUpdateDecorrenzaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CannotUpdateDecorrenzaException(String message, Throwable cause) {
		super(message, cause);
	}

	public CannotUpdateDecorrenzaException(String message) {
		super(message);
	}

	public CannotUpdateDecorrenzaException(Throwable cause) {
		super(cause);
	}

}
