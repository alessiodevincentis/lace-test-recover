package com.woonders.lacemscommon.exception;

public class FileExcelToImportNotValidException extends Exception {

	public FileExcelToImportNotValidException() {
		super();
	}

	public FileExcelToImportNotValidException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FileExcelToImportNotValidException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileExcelToImportNotValidException(String message) {
		super(message);
	}

	public FileExcelToImportNotValidException(Throwable cause) {
		super(cause);
	}

}
