package com.woonders.lacemscommon.exception;

public class RinnovoNotSavedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 872451511559781301L;

	public RinnovoNotSavedException() {
		super();
	}

	public RinnovoNotSavedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RinnovoNotSavedException(String message, Throwable cause) {
		super(message, cause);
	}

	public RinnovoNotSavedException(String message) {
		super(message);
	}

	public RinnovoNotSavedException(Throwable cause) {
		super(cause);
	}

}
