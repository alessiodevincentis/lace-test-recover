package com.woonders.lacemscommon.exception;

public class CannotDeletePraticaException extends Exception {

	public CannotDeletePraticaException() {
		super();
	}

	public CannotDeletePraticaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CannotDeletePraticaException(String message, Throwable cause) {
		super(message, cause);
	}

	public CannotDeletePraticaException(String message) {
		super(message);
	}

	public CannotDeletePraticaException(Throwable cause) {
		super(cause);
	}

}
