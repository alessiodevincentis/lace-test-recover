package com.woonders.lacemscommon.exception;

public class SecondaOccupazioneNotDeleteException extends Exception {

	public SecondaOccupazioneNotDeleteException() {
		super();
	}

	public SecondaOccupazioneNotDeleteException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SecondaOccupazioneNotDeleteException(String message, Throwable cause) {
		super(message, cause);
	}

	public SecondaOccupazioneNotDeleteException(String message) {
		super(message);
	}

	public SecondaOccupazioneNotDeleteException(Throwable cause) {
		super(cause);
	}

}
